package com.rafael.devcursojava.aula13;

public class OperadoreRelacionais {
	public static void main(String[] args) {
		
		int valor1 = 1, valor2 = 2;
		
		System.out.println("Valor 1: " + valor1 + "\nValor 2: " + valor2);
		
		System.out.println("Valor 1 == Valor 2: " + (valor1 == valor2));
		
		System.out.println("Valor 1 != Valor 2: " + (valor1 != valor2));
		
		System.out.println("Valor 1 > Valor 2: " + (valor1 > valor2));
		
		System.out.println("Valor 1 < Valor 2: " + (valor1 < valor2));
		
		System.out.println("Valor 1 >= Valor 2: " + (valor1 >= valor2));
		
		System.out.println("Valor 1 <= Valor 2: " + (valor1 <= valor2));
		
		
	}
}
